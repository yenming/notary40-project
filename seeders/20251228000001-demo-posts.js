function generateSlug(title, index) {
  // 使用标题和索引确保唯一性
  const baseSlug = title
    .toLowerCase()
    .replace(/[^\w\s-]/g, "")
    .replace(/\s+/g, "-")
    .replace(/-+/g, "-")
    .trim();
  // 如果标题全是中文，slug 会是空的，所以添加索引
  return baseSlug || `post-${index}`;
}

